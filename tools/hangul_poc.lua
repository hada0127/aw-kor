local frame=0
local shot=false
local function nav()
  -- press Start/A pattern to reach game-select
  if frame==40 or frame==90 then emu:setKeys(8)        -- Start
  elseif frame==46 or frame==96 then emu:setKeys(0)
  end
end
local function paint()
  -- overwrite BG VRAM (charblocks 0-1, 1024 tiles) with cycling Hangul
  for t=0,1023 do
    local hi=(t % #H)+1
    local g=H[hi]
    local b=t*32
    for i=0,31 do emu.memory.vram:write8(b+i, g[i+1]) end
  end
end
callbacks:add("frame", function()
  frame=frame+1
  nav()
  if frame>=140 then
    paint()
    if frame==150 and not shot then
      shot=true
      emu:screenshot("/tmp/lua_hangul.png")
      console:log("HANGUL SHOT taken at frame "..frame)
    end
  end
end)
